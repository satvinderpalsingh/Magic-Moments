import mongoose from 'mongoose';

const postSchema=mongoose.Schema({
    title: String,
    message: String,
    creator: String,
    tags: [String],
    selectedFile: String,
    likeCount: {
        type: Number,
        default: 0,
    },
    createdAt: {
        type: Date,
        default: new Date(),
    },
});

const PostMemory=mongoose.model('PostMemory',postSchema);
export default PostMemory;